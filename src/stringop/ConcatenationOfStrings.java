package stringop;

public class ConcatenationOfStrings {

	public static void main(String[] args) {
		String str = new String("prathamesh");
		String str1 = new String("dhasade");
		System.out.println(str.concat(str1));
		System.out.println(str.concat(" " + str1)); //combine two string and make one string

	}

}
